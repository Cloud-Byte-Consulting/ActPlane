DEFAULT_ACTPLANE = ROOT / "target" / "release" / "actplane"


@dataclass(frozen=True)
class PerfConfig:
    name: str
    rules: int
    sources: int
    transforms: int


CONFIGS = {
    "ap-1": PerfConfig("ap-1", rules=1, sources=2, transforms=0),
    "ap-10": PerfConfig("ap-10", rules=10, sources=5, transforms=0),
    "ap-32": PerfConfig("ap-32", rules=32, sources=16, transforms=8),
    "ap-100": PerfConfig("ap-100", rules=100, sources=32, transforms=16),
}


@dataclass(frozen=True)
class Workload:
    name: str
    argv: list[str]
    cwd: Path = ROOT
    env: dict[str, str] | None = None


def utc_stamp() -> str:
    return (
        datetime.now(timezone.utc)
        .replace(microsecond=0)
        .isoformat()
        .replace("+00:00", "Z")
        .replace(":", "")
    )


def parse_csv_arg(value: str) -> list[str]:
    return [x.strip() for x in value.split(",") if x.strip()]


def run(
    cmd: list[str],
    cwd: Path = ROOT,
    timeout: int = 600,
    env: dict[str, str] | None = None,
) -> subprocess.CompletedProcess[str]:
    merged_env = os.environ.copy()
    if env:
        merged_env.update(env)
    return subprocess.run(
        cmd,
        cwd=cwd,
        env=merged_env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        timeout=timeout,
    )


def build_actplane() -> None:
    proc = run(
        ["cargo", "build", "--release", "-p", "actplane"],
        cwd=ROOT,
        timeout=1200,
    )
    if proc.returncode != 0:
        raise RuntimeError(f"cargo build failed:\n{proc.stdout}\n{proc.stderr}")


def generate_policy(cfg: PerfConfig) -> str:
    lines = [
        "version: 1",
        "policy: |",
        "  # Generated by docs/rq2-performance/run_macro.py.",
        "  # No-hit policy: the workload subtree is labeled, but rule targets",
        "  # are deliberately unreachable so no violation/reporting cost is included.",
        '  source COMMAND = exec "**"',
    ]
    for i in range(1, cfg.sources):
        lines.append(f'  source L{i} = file "/tmp/actplane-macro/source-{i}"')
    for i in range(cfg.transforms):
        label = f"L{1 + (i % max(1, cfg.sources - 1))}"
        lines.append(f'  declassify {label} by exec "__actplane_macro_clear_{i}"')

    op_cycle = ["open", "write", "connect", "exec"]
    for i in range(cfg.rules):
        op = op_cycle[i % len(op_cycle)]
        expr = "COMMAND"
        if cfg.sources > 1:
            expr = f"COMMAND and not L{1 + (i % (cfg.sources - 1))}"
        lines.append("")
        lines.append(f"  rule macro-nohit-{i}:")
        if op == "open":
            lines.append(f'    notify open file "/tmp/actplane-macro/nohit/open-{i}" if {expr}')
        elif op == "write":
            lines.append(f'    notify write file "/tmp/actplane-macro/nohit/write-{i}" if {expr}')
        elif op == "connect":
            octet = 1 + (i % 250)
            lines.append(f'    notify connect endpoint "203.0.113.{octet}" if {expr}')
        else:
            lines.append(f'    notify exec "__actplane_macro_nohit_{i}" if {expr}')
        lines.append('    because "macro performance no-hit rule"')
    return "\n".join(lines) + "\n"


def write_policies(configs: list[str], out_dir: Path) -> dict[str, Path]:
    policy_dir = out_dir / "policies"
    policy_dir.mkdir(parents=True, exist_ok=True)
    out: dict[str, Path] = {}
    for name in configs:
        if name == "baseline":
            continue
        path = policy_dir / f"{name}.yaml"
        path.write_text(generate_policy(CONFIGS[name]), encoding="utf-8")
        out[name] = path
    return out


def metadata(args: argparse.Namespace, out_dir: Path) -> dict[str, Any]:
    def first_line(path: Path) -> str:
        try:
            return path.read_text(encoding="utf-8", errors="replace").splitlines()[0]
        except (OSError, IndexError):
            return ""

    def maybe(cmd: list[str]) -> str:
        proc = run(cmd, timeout=30)
        return proc.stdout.strip() if proc.returncode == 0 else proc.stderr.strip()

    return {
        "created_at": utc_stamp(),
        "argv": sys.argv,
        "root": str(ROOT),
        "output_dir": str(out_dir),
        "active_lsms": first_line(Path("/sys/kernel/security/lsm")),
        "uname": maybe(["uname", "-a"]),
        "cpu": maybe(["lscpu"]),
        "git_rev": maybe(["git", "rev-parse", "HEAD"]),
        "git_status_porcelain": maybe(["git", "status", "--porcelain"]),
        "actplane": str(args.actplane),
        "stress_ng_version": maybe(["stress-ng", "--version"]),
        "perf_status": maybe(["perf", "stat", "--", "true"]),
    }


